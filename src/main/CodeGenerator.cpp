#include "CodeGenerator.hpp"
#include <iostream>
#include <iomanip>
#include <sstream>

CodeGenerator::CodeGenerator(std::ostream& out) : out(out) {}

void CodeGenerator::setSourceInfo(const std::string& filename, const std::vector<std::string>& lines) {
    sourceFilename = filename;
    sourceLines = lines;
}

void CodeGenerator::embedSource(ASTNode& node) {
    if (node.line <= 0 || node.line > (int)sourceLines.size()) return;
    if (node.line == lastEmbeddedLine) return;
    lastEmbeddedLine = node.line;
    out << "; [" << sourceFilename << ":" << node.line << "] " << sourceLines[node.line - 1] << std::endl;
}

void CodeGenerator::generate(TranslationUnit& unit) {
    emitter = std::make_unique<M65Emitter>(out, zeroPageStart);
    zpRegs.clear();
    for (uint32_t i = 0; i < zeroPageAvail; ++i) zpRegs.push_back({false});
    invalidateRegs();
    resultNeeded = true;
    unit.accept(*this);
    emitData();
}

void CodeGenerator::emit(const std::string& line) {
    out << "    " << line << std::endl;
}

std::string CodeGenerator::newLabel() {
    return "L" + std::to_string(labelCount++);
}

std::string CodeGenerator::newDontCareLabel() {
    return "@L" + std::to_string(labelCount++);
}

bool CodeGenerator::isStruct(const std::string& type) {
    return type.length() >= 7 && type.substr(0, 7) == "struct ";
}

CodeGenerator::ExpressionType CodeGenerator::getExprType(Expression* expr) {
    if (auto* ref = dynamic_cast<VariableReference*>(expr)) {
        if (variableTypes.count(ref->name)) {
            return {variableTypes.at(ref->name).type, variableTypes.at(ref->name).pointerLevel};
        }
    }
    if (auto* bin = dynamic_cast<BinaryOperation*>(expr)) { return getExprType(bin->left.get()); }
    if (auto* un = dynamic_cast<UnaryOperation*>(expr)) {
        CodeGenerator::ExpressionType sub = getExprType(un->operand.get());
        if (un->op == "*") return {sub.type, sub.pointerLevel > 0 ? sub.pointerLevel - 1 : 0};
        if (un->op == "&") return {sub.type, sub.pointerLevel + 1};
        return sub;
    }
    if (auto* ma = dynamic_cast<MemberAccess*>(expr)) {
        ExpressionType baseType = getExprType(ma->structExpr.get());
        if (isStruct(baseType.type)) {
            std::string sName = baseType.type.substr(7);
            if (structs.count(sName)) {
                StructInfo& sInfo = structs[sName];
                if (sInfo.members.count(ma->memberName)) {
                    MemberInfo& mInfo = sInfo.members[ma->memberName];
                    return {mInfo.type, mInfo.pointerLevel};
                }
            }
        }
    }
    if (auto* lit = dynamic_cast<IntegerLiteral*>(expr)) { return {"int", 0}; }
    return {"int", 0};
}

void CodeGenerator::emitAddress(Expression* expr) {
    if (auto* ref = dynamic_cast<VariableReference*>(expr)) {
        emitter->tsx();
        emitter->txa();
        if (flags.carry != TriState::CLEAR) emitter->clc();
        updateFlags(TriState::CLEAR, TriState::UNKNOWN, TriState::UNKNOWN);
        emit("ADC #" + ref->name);
        invalidateFlags();
        emitter->pha();
        emitter->lda_imm(0);
        updateRegA(0);
        emitter->adc_imm(0);
        invalidateFlags();
        emitter->tax();
        updateRegXVar("unknown", 0);
        emitter->pla();
    } else if (auto* ma = dynamic_cast<MemberAccess*>(expr)) {
        if (ma->isArrow) {
            bool oldNeeded = resultNeeded;
            resultNeeded = true;
            ma->structExpr->accept(*this);
            resultNeeded = oldNeeded;
        } else {
            emitAddress(ma->structExpr.get());
        }
        ExpressionType baseType = getExprType(ma->structExpr.get());
        if (isStruct(baseType.type)) {
            std::string sName = baseType.type.substr(7);
            if (structs.count(sName)) {
                StructInfo& sInfo = structs[sName];
                if (sInfo.members.count(ma->memberName)) {
                    MemberInfo& mInfo = sInfo.members[ma->memberName];
                    if (mInfo.offset > 0) {
                        emitter->add_16_imm(mInfo.offset);
                    }
                }
            }
        }
    } else if (auto* un = dynamic_cast<UnaryOperation*>(expr)) {
        if (un->op == "*") {
            bool oldNeeded = resultNeeded;
            resultNeeded = true;
            un->operand->accept(*this);
            resultNeeded = oldNeeded;
        }
    }
    invalidateRegs();
}

void CodeGenerator::visit(TranslationUnit& node) {
    out << "; Generated by cc45" << std::endl;
    out << ".org $2000" << std::endl << std::endl;
    for (auto& decl : node.topLevelDecls) decl->accept(*this);
}

void CodeGenerator::visit(FunctionDeclaration& node) {
    out << "PROC " << node.name << std::endl;
    variableTypes.clear();
    currentVars.clear();
    std::string procLine = "    .proc " + node.name;
    for (auto& param : node.parameters) {
        variableTypes[param.name] = {param.type, param.pointerLevel};
        if (param.pointerLevel > 0) {
            procLine += ", W#" + param.name;
        } else if (param.type == "char") {
            procLine += ", B#" + param.name;
        } else {
            procLine += ", W#" + param.name;
        }
        currentVars.push_back(param.name);
    }
    out << procLine << std::endl;
    node.body->accept(*this);
    emit("ENDPROC");
    out << std::endl;
}

void CodeGenerator::visit(CompoundStatement& node) {
    for (auto& stmt : node.statements) stmt->accept(*this);
}

void CodeGenerator::visit(VariableDeclaration& node) {
    embedSource(node);
    variableTypes[node.name] = {node.type, node.pointerLevel};
    int size = 0;
    if (node.pointerLevel > 0) size = 2;
    else if (node.type == "char") size = 1;
    else if (node.type == "int") size = 2;
    else if (isStruct(node.type)) {
        std::string sName = node.type.substr(7);
        if (structs.count(sName)) size = structs[sName].totalSize;
        else throw std::runtime_error("Unknown struct type: " + sName);
    }

    if (node.initializer) {
        bool oldNeeded = resultNeeded;
        resultNeeded = true;
        node.initializer->accept(*this);
        resultNeeded = oldNeeded;
        if (size == 2) emitter->push_ax();
        else emitter->pha();
    } else {
        if (size == 2) emit("PHW #$0000");
        else { emitter->lda_imm(0); emitter->pha(); }
    }
    for (const auto& varName : currentVars) {
        emit(".var " + varName + " = " + varName + " + " + std::to_string(size));
    }
    emit(".var " + node.name + " = " + std::to_string(size));
    currentVars.push_back(node.name);
    emit(".cleanup " + std::to_string(size));
    invalidateRegs();
}

void CodeGenerator::visit(Assignment& node) {
    embedSource(node);
    
    if (auto* ref = dynamic_cast<VariableReference*>(node.target.get())) {
        if (auto* bin = dynamic_cast<BinaryOperation*>(node.expression.get())) {
            if ((bin->op == "+" || bin->op == "-") && 
                dynamic_cast<IntegerLiteral*>(bin->right.get()) && 
                dynamic_cast<IntegerLiteral*>(bin->right.get())->value == 1) {
                if (auto* sourceRef = dynamic_cast<VariableReference*>(bin->left.get())) {
                    if (ref->name == sourceRef->name) {
                        VarInfo vi = variableTypes[ref->name];
                        bool is16Bit = (vi.pointerLevel > 0 || vi.type == "int" || (isStruct(vi.type) && structs[vi.type.substr(7)].totalSize > 1));
                        if (bin->op == "+") {
                            if (is16Bit) emit("INW " + ref->name + ", s");
                            else emit("INC " + ref->name + ", s");
                        } else {
                            if (is16Bit) emit("DEW " + ref->name + ", s");
                            else emit("DEC " + ref->name + ", s");
                        }
                        invalidateRegs();
                        if (resultNeeded) ref->accept(*this);
                        return;
                    }
                }
            }
        }

        bool oldNeeded = resultNeeded;
        resultNeeded = true;
        node.expression->accept(*this);
        resultNeeded = oldNeeded;

        emit("STA " + ref->name + ", s");
        updateRegAVar(ref->name, 0);

        VarInfo vi = variableTypes[ref->name];
        if (vi.pointerLevel > 0 || vi.type == "int" || (isStruct(vi.type) && structs[vi.type.substr(7)].totalSize > 1)) {
            emitter->pha();
            emitter->txa();
            emit("STA " + ref->name + "+1, s");
            updateRegXVar(ref->name, 1);
            emitter->pla();
        }
        invalidateRegs();
        return;
    } else if (auto* ma = dynamic_cast<MemberAccess*>(node.target.get())) {
        if (!ma->isArrow) {
            if (auto* ref = dynamic_cast<VariableReference*>(ma->structExpr.get())) {
                ExpressionType baseType = getExprType(ref);
                if (isStruct(baseType.type)) {
                    std::string sName = baseType.type.substr(7);
                    if (structs.count(sName) && structs[sName].members.count(ma->memberName)) {
                        MemberInfo& mInfo = structs[sName].members[ma->memberName];
                        bool oldNeeded = resultNeeded;
                        resultNeeded = true;
                        node.expression->accept(*this);
                        resultNeeded = oldNeeded;
                        emit("STA " + ref->name + "+" + std::to_string(mInfo.offset) + ", s");
                        updateRegAVar(ref->name, mInfo.offset);
                        if (mInfo.pointerLevel > 0 || mInfo.type == "int" || (isStruct(mInfo.type) && structs[mInfo.type.substr(7)].totalSize > 1)) {
                            emitter->pha();
                            emitter->txa();
                            emit("STA " + ref->name + "+" + std::to_string(mInfo.offset + 1) + ", s");
                            updateRegXVar(ref->name, mInfo.offset + 1);
                            emitter->pla();
                        }
                        invalidateRegs();
                        return;
                    }
                }
            }
        }
    }

    emitAddress(node.target.get());
    int zpIdx = allocateZP(2);
    emitter->sta_zp(emitter->getZP(zpIdx));
    emitter->stx_zp(emitter->getZP(zpIdx + 1));
    bool oldNeeded = resultNeeded;
    resultNeeded = true;
    node.expression->accept(*this);
    resultNeeded = oldNeeded;
    emitter->sta_ind_z(emitter->getZP(zpIdx), false);
    updateRegY(0);
    ExpressionType targetType = getExprType(node.target.get());
    if (targetType.pointerLevel > 0 || targetType.type == "int" || (isStruct(targetType.type) && structs[targetType.type.substr(7)].totalSize > 1)) {
        emitter->txa();
        regA.known = false;
        emitter->ldy_imm(1);
        updateRegY(1);
        std::stringstream ss;
        ss << std::hex << std::uppercase << std::setfill('0') << std::setw(2) << (int)emitter->getZP(zpIdx);
        emit("STA ($" + ss.str() + "),Y");
    }
    freeZP(zpIdx, 2);
    invalidateRegs();
}

void CodeGenerator::visit(ReturnStatement& node) {
    embedSource(node);
    if (node.expression) {
        bool oldNeeded = resultNeeded;
        resultNeeded = true;
        node.expression->accept(*this);
        resultNeeded = oldNeeded;
    }
    emit("RTN #$00");
    invalidateRegs();
}

void CodeGenerator::visit(ExpressionStatement& node) {
    embedSource(node);
    bool oldNeeded = resultNeeded;
    resultNeeded = false;
    node.expression->accept(*this);
    resultNeeded = oldNeeded;
}

void CodeGenerator::visit(IfStatement& node) {
    embedSource(node);
    bool oldNeeded = resultNeeded;
    resultNeeded = true;
    node.condition->accept(*this);
    resultNeeded = oldNeeded;

    std::string labelElse = newLabel();
    std::string labelEnd = newLabel();

    if (flags.znSource == FlagSource::A) {
        // Flags already set for A, skip CMP #0
    } else {
        emit("CMP #$00");
        updateZNFlags(FlagSource::A);
    }

    emit("BNE *+5");
    emitter->txa();
    updateZNFlags(FlagSource::A); // TXA sets flags for A
    emit("BEQ " + labelElse);

    node.thenBranch->accept(*this);
    if (node.elseBranch) {
        emit("BRA " + labelEnd);
        out << labelElse << ":" << std::endl;
        invalidateRegs();
        node.elseBranch->accept(*this);
        out << labelEnd << ":" << std::endl;
    } else {
        out << labelElse << ":" << std::endl;
    }
    invalidateRegs();
}

void CodeGenerator::visit(WhileStatement& node) {
    embedSource(node);
    std::string labelStart = newLabel();
    std::string labelEnd = newLabel();
    out << labelStart << ":" << std::endl;
    invalidateRegs();
    
    bool oldNeeded = resultNeeded;
    resultNeeded = true;
    node.condition->accept(*this);
    resultNeeded = oldNeeded;

    if (flags.znSource == FlagSource::A) {
    } else {
        emit("CMP #$00");
        updateZNFlags(FlagSource::A);
    }
    emit("BNE *+5");
    emitter->txa();
    updateZNFlags(FlagSource::A);
    emit("BEQ " + labelEnd);

    node.body->accept(*this);
    emit("BRA " + labelStart);
    out << labelEnd << ":" << std::endl;
    invalidateRegs();
}

void CodeGenerator::visit(DoWhileStatement& node) {
    embedSource(node);
    std::string labelStart = newLabel();
    out << labelStart << ":" << std::endl;
    node.body->accept(*this);
    
    bool oldNeeded = resultNeeded;
    resultNeeded = true;
    node.condition->accept(*this);
    resultNeeded = oldNeeded;

    if (flags.znSource != FlagSource::A) {
        emit("CMP #$00");
    }
    emit("BNE " + labelStart);
    emitter->txa();
    updateZNFlags(FlagSource::A);
    emit("BNE " + labelStart);
    invalidateRegs();
}

void CodeGenerator::visit(ForStatement& node) {
    embedSource(node);
    if (node.initializer) {
        bool oldNeeded = resultNeeded;
        resultNeeded = false;
        node.initializer->accept(*this);
        resultNeeded = oldNeeded;
    }
    std::string labelStart = newLabel();
    std::string labelEnd = newLabel();
    out << labelStart << ":" << std::endl;
    if (node.condition) {
        bool oldNeeded = resultNeeded;
        resultNeeded = true;
        node.condition->accept(*this);
        resultNeeded = oldNeeded;

        if (flags.znSource == FlagSource::A) {
        } else {
            emit("CMP #$00");
            updateZNFlags(FlagSource::A);
        }
        emit("BNE *+5");
        emitter->txa();
        updateZNFlags(FlagSource::A);
        emit("BEQ " + labelEnd);
    }
    node.body->accept(*this);
    if (node.increment) {
        bool oldNeeded = resultNeeded;
        resultNeeded = false;
        node.increment->accept(*this);
        resultNeeded = oldNeeded;
    }
    emit("BRA " + labelStart);
    out << labelEnd << ":" << std::endl;
    invalidateRegs();
}

void CodeGenerator::visit(StructDefinition& node) {
    StructInfo info;
    info.name = node.name;
    int currentOffset = 0;
    for (auto& member : node.members) {
        MemberInfo mInfo;
        mInfo.type = member.type;
        mInfo.pointerLevel = member.pointerLevel;
        mInfo.offset = currentOffset;
        info.members[member.name] = mInfo;
        if (member.pointerLevel > 0) currentOffset += 2;
        else if (member.type == "char") currentOffset += 1;
        else if (member.type == "int") currentOffset += 2;
        else if (isStruct(member.type)) {
            std::string sName = member.type.substr(7);
            if (structs.count(sName)) currentOffset += structs[sName].totalSize;
            else throw std::runtime_error("Unknown struct type in member: " + sName);
        }
    }
    info.totalSize = currentOffset;
    structs[node.name] = info;
}

void CodeGenerator::visit(MemberAccess& node) {
    ExpressionType baseType = getExprType(node.structExpr.get());
    if (!isStruct(baseType.type)) throw std::runtime_error("Dot/Arrow operator on non-struct type");
    std::string sName = baseType.type.substr(7);
    if (!structs.count(sName)) throw std::runtime_error("Unknown struct type: " + sName);
    StructInfo& sInfo = structs[sName];
    if (!sInfo.members.count(node.memberName)) throw std::runtime_error("Member " + node.memberName + " not found in struct " + sName);
    MemberInfo& mInfo = sInfo.members[node.memberName];

    if (!node.isArrow) {
        if (auto* ref = dynamic_cast<VariableReference*>(node.structExpr.get())) {
            embedSource(node);
            if (!resultNeeded) return;
            if (regA.known && regA.isVariable && regA.varName == ref->name && regA.varOffset == mInfo.offset) {
            } else {
                emit("LDA " + ref->name + "+" + std::to_string(mInfo.offset) + ", s");
                updateRegAVar(ref->name, mInfo.offset);
            }
            if (mInfo.pointerLevel > 0 || mInfo.type == "int" || (isStruct(mInfo.type) && structs[mInfo.type.substr(7)].totalSize > 1)) {
                if (regX.known && regX.isVariable && regX.varName == ref->name && regX.varOffset == mInfo.offset + 1) {
                } else {
                    emitter->pha();
                    emit("LDA " + ref->name + "+" + std::to_string(mInfo.offset + 1) + ", s");
                    emitter->tax();
                    emitter->pla();
                    updateRegXVar(ref->name, mInfo.offset + 1);
                }
            } else {
                if (!regX.known || regX.isVariable || regX.value != 0) {
                    emitter->ldx_imm(0);
                    updateRegX(0);
                }
            }
            return;
        }
    }

    if (node.isArrow) {
        bool oldNeeded = resultNeeded;
        resultNeeded = true;
        node.structExpr->accept(*this);
        resultNeeded = oldNeeded;
    } else {
        emitAddress(node.structExpr.get());
    }
    if (!resultNeeded) { invalidateRegs(); return; }

    if (mInfo.offset > 0) emitter->add_16_imm(mInfo.offset);
    int zpIdx = allocateZP(2);
    emitter->sta_zp(emitter->getZP(zpIdx));
    emitter->stx_zp(emitter->getZP(zpIdx + 1));
    emitter->lda_ind_z(emitter->getZP(zpIdx), false);
    updateRegY(0);
    regA.known = false;
    if (mInfo.pointerLevel > 0 || mInfo.type == "int" || (isStruct(mInfo.type) && structs[mInfo.type.substr(7)].totalSize > 1)) {
        emitter->pha();
        emitter->ldy_imm(1);
        updateRegY(1);
        std::stringstream ss;
        ss << std::hex << std::uppercase << std::setfill('0') << std::setw(2) << (int)emitter->getZP(zpIdx);
        emit("LDA ($" + ss.str() + "),Y");
        regA.known = false;
        emitter->tax();
        emitter->pla();
    } else {
        emitter->ldx_imm(0);
        updateRegX(0);
    }
    freeZP(zpIdx, 2);
    invalidateRegs();
}

void CodeGenerator::visit(IntegerLiteral& node) {
    if (!resultNeeded) return;
    uint8_t low = node.value & 0xFF;
    uint8_t high = (node.value >> 8) & 0xFF;
    if (!regA.known || regA.isVariable || regA.value != low) {
        emitter->lda_imm(low);
        updateRegA(low);
    }
    if (!regX.known || regX.isVariable || regX.value != high) {
        emitter->ldx_imm(high);
        updateRegX(high);
    }
}

void CodeGenerator::visit(StringLiteral& node) {
    if (!resultNeeded) return;
    if (stringPool.find(node.value) == stringPool.end()) stringPool[node.value] = "STR" + std::to_string(stringCount++);
    std::string label = stringPool[node.value];
    emit("LDA #<" + label); emit("LDX #>" + label);
    invalidateRegs();
}

void CodeGenerator::visit(VariableReference& node) {
    if (!resultNeeded) return;
    if (regA.known && regA.isVariable && regA.varName == node.name && regA.varOffset == 0) {
    } else {
        emit("LDA " + node.name + ", s");
        updateRegAVar(node.name, 0);
    }
    if (variableTypes.count(node.name) && variableTypes[node.name].type == "char" && variableTypes[node.name].pointerLevel == 0) {
        if (!regX.known || regX.isVariable || regX.value != 0) {
            emitter->ldx_imm(0);
            updateRegX(0);
        }
    } else {
        if (regX.known && regX.isVariable && regX.varName == node.name && regX.varOffset == 1) {
        } else {
            emit("PHA");
            emit("LDA " + node.name + "+1, s");
            emit("TAX");
            emit("PLA");
            updateRegXVar(node.name, 1);
        }
    }
}

void CodeGenerator::visit(FunctionCall& node) {
    bool oldNeeded = resultNeeded;
    resultNeeded = true;
    for (auto& arg : node.arguments) {
        arg->accept(*this);
        emitter->push_ax();
    }
    resultNeeded = oldNeeded;
    emit("JSR " + node.name);
    if (node.arguments.size() > 0) emit("RTN #" + std::to_string(node.arguments.size() * 2));
    invalidateRegs();
}

void CodeGenerator::emitData() {
    out << std::endl << "; Data Section" << std::endl;
    for (const auto& entry : stringPool) {
        out << entry.second << ":" << std::endl;
        out << "    .text \"" << entry.first << "\"" << std::endl;
        out << "    .byte 0" << std::endl;
    }
}

void CodeGenerator::visit(BinaryOperation& node) {
    ExpressionType lhsType = getExprType(node.left.get());
    ExpressionType rhsType = getExprType(node.right.get());
    int scale = 1;
    if (lhsType.pointerLevel > 0 && rhsType.pointerLevel == 0 && (node.op == "+" || node.op == "-")) {
        scale = (lhsType.type == "char" && lhsType.pointerLevel == 1) ? 1 : 2;
    }
    bool isLiteralOne = false;
    if (scale == 1) {
        if (auto* lit = dynamic_cast<IntegerLiteral*>(node.right.get())) {
            if (lit->value == 1) isLiteralOne = true;
        }
    }
    if (isLiteralOne && (node.op == "+" || node.op == "-")) {
        bool oldNeeded = resultNeeded;
        resultNeeded = true;
        node.left->accept(*this);
        resultNeeded = oldNeeded;
        if (node.op == "+") {
            std::string label = newDontCareLabel();
            emitter->inc_a(); emitter->bne(0x02); emit("INX");
            out << label << ":" << std::endl;
        } else {
            std::string label = newDontCareLabel();
            emit("CMP #$00"); emit("BNE " + label); emit("DEX");
            out << label << ":" << std::endl; emitter->dec_a();
        }
        invalidateRegs();
        return;
    }
    bool oldNeeded = resultNeeded;
    resultNeeded = true;
    node.left->accept(*this);
    int zpIdx = allocateZP(2);
    emitter->sta_zp(emitter->getZP(zpIdx));
    emitter->stx_zp(emitter->getZP(zpIdx + 1));
    node.right->accept(*this);
    resultNeeded = oldNeeded;
    if (scale > 1) {
        emitter->asl_a();
        emitter->pha();
        emitter->txa();
        emitter->rol_a();
        emitter->tax();
        emitter->pla();
    }
    if (node.op == "+") {
        if (flags.carry != TriState::CLEAR) emitter->clc();
        updateFlags(TriState::CLEAR, TriState::UNKNOWN, TriState::UNKNOWN);
        emitter->adc_zp(emitter->getZP(zpIdx));
        invalidateFlags();
        emitter->pha();
        emitter->txa();
        emitter->adc_zp(emitter->getZP(zpIdx + 1));
        emitter->tax();
        emitter->pla();
        invalidateFlags();
    } else if (node.op == "-") {
        int tempIdx = allocateZP(1);
        emitter->sta_zp(emitter->getZP(tempIdx)); 
        emitter->lda_zp(emitter->getZP(zpIdx));
        if (flags.carry != TriState::SET) emitter->sec();
        updateFlags(TriState::SET, TriState::UNKNOWN, TriState::UNKNOWN);
        emitter->sbc_zp(emitter->getZP(tempIdx));
        invalidateFlags();
        emitter->pha();
        emitter->txa(); 
        emitter->sta_zp(emitter->getZP(tempIdx)); 
        emitter->lda_zp(emitter->getZP(zpIdx + 1));
        emitter->sbc_zp(emitter->getZP(tempIdx));
        emitter->tax();
        emitter->pla();
        invalidateFlags();
        freeZP(tempIdx, 1);
    } else if (node.op == "&") {
        emitter->and_zp(emitter->getZP(zpIdx));
        emitter->pha();
        emitter->txa();
        emitter->and_zp(emitter->getZP(zpIdx + 1));
        emitter->tax();
        emitter->pla();
    } else if (node.op == "|") {
        emitter->ora_zp(emitter->getZP(zpIdx));
        emitter->pha();
        emitter->txa();
        emitter->ora_zp(emitter->getZP(zpIdx + 1));
        emitter->tax();
        emitter->pla();
    } else if (node.op == "^") {
        emitter->eor_zp(emitter->getZP(zpIdx));
        emitter->pha();
        emitter->txa();
        emitter->eor_zp(emitter->getZP(zpIdx + 1));
        emitter->tax();
        emitter->pla();
    } else if (node.op == "==" || node.op == "!=") {
        emitter->cmp_zp(emitter->getZP(zpIdx));
        invalidateFlags();
        std::string labelFalse = newDontCareLabel(); 
        std::string labelEnd = newDontCareLabel();
        if (node.op == "==") emit("BNE " + labelFalse); else emit("BEQ " + labelFalse);
        emitter->txa();
        invalidateFlags();
        emitter->cmp_zp(emitter->getZP(zpIdx + 1));
        invalidateFlags();
        if (node.op == "==") emit("BNE " + labelFalse); else emit("BEQ " + labelFalse);
        emitter->lda_imm(1); emitter->bra(0x02);
        out << labelFalse << ":" << std::endl; emitter->lda_imm(0);
        out << labelEnd << ":" << std::endl; 
        invalidateRegs();
        emitter->ldx_imm(0);
        updateRegX(0);
    }
    freeZP(zpIdx, 2);
    invalidateRegs();
}

void CodeGenerator::visit(UnaryOperation& node) {
    if (node.op == "*") {
        bool oldNeeded = resultNeeded;
        resultNeeded = true;
        node.operand->accept(*this);
        resultNeeded = oldNeeded;
        if (!resultNeeded) { invalidateRegs(); return; }
        int zpIdx = allocateZP(2);
        emitter->sta_zp(emitter->getZP(zpIdx));
        emitter->stx_zp(emitter->getZP(zpIdx + 1));
        emitter->lda_ind_z(emitter->getZP(zpIdx), false);
        updateRegY(0);
        regA.known = false;
        ExpressionType subType = getExprType(node.operand.get());
        if (subType.type == "char" && subType.pointerLevel == 1) {
            emitter->ldx_imm(0);
            updateRegX(0);
        } else {
            emitter->pha();
            emitter->ldy_imm(1);
            updateRegY(1);
            std::stringstream ss;
            ss << std::hex << std::uppercase << std::setfill('0') << std::setw(2) << (int)emitter->getZP(zpIdx);
            emit("LDA ($" + ss.str() + "),Y");
            regA.known = false;
            emitter->tax();
            emitter->pla();
        }
        freeZP(zpIdx, 2);
    } else if (node.op == "&") {
        emitAddress(node.operand.get());
    } else if (node.op == "++" || node.op == "--" || node.op == "++_POST" || node.op == "--_POST") {
        bool isInc = (node.op.substr(0, 2) == "++");
        bool isPost = (node.op.find("_POST") != std::string::npos);
        if (auto* ref = dynamic_cast<VariableReference*>(node.operand.get())) {
            VarInfo vi = variableTypes[ref->name];
            bool is16Bit = (vi.pointerLevel > 0 || vi.type == "int" || (isStruct(vi.type) && structs[vi.type.substr(7)].totalSize > 1));
            if (isPost && resultNeeded) ref->accept(*this);
            invalidateRegs();
            if (is16Bit) {
                if (isInc) emit("INW " + ref->name + ", s");
                else emit("DEW " + ref->name + ", s");
            } else {
                if (isInc) emit("INC " + ref->name + ", s");
                else emit("DEC " + ref->name + ", s");
            }
            if (!isPost && resultNeeded) ref->accept(*this);
        } else if (auto* ma = dynamic_cast<MemberAccess*>(node.operand.get())) {
            if (!ma->isArrow) {
                if (auto* ref = dynamic_cast<VariableReference*>(ma->structExpr.get())) {
                    ExpressionType baseType = getExprType(ref);
                    if (isStruct(baseType.type)) {
                        std::string sName = baseType.type.substr(7);
                        if (structs.count(sName) && structs[sName].members.count(ma->memberName)) {
                            MemberInfo& mInfo = structs[sName].members[ma->memberName];
                            bool is16Bit = (mInfo.pointerLevel > 0 || mInfo.type == "int" || (isStruct(mInfo.type) && structs[mInfo.type.substr(7)].totalSize > 1));
                            if (isPost && resultNeeded) node.operand->accept(*this);
                            invalidateRegs();
                            if (is16Bit) {
                                if (isInc) emit("INW " + ref->name + "+" + std::to_string(mInfo.offset) + ", s");
                                else emit("DEW " + ref->name + "+" + std::to_string(mInfo.offset) + ", s");
                            } else {
                                if (isInc) emit("INC " + ref->name + "+" + std::to_string(mInfo.offset) + ", s");
                                else emit("DEC " + ref->name + "+" + std::to_string(mInfo.offset) + ", s");
                            }
                            if (!isPost && resultNeeded) node.operand->accept(*this);
                        }
                    }
                }
            } else {
                bool oldNeeded = resultNeeded;
                resultNeeded = true;
                node.operand->accept(*this);
                resultNeeded = oldNeeded;
                invalidateRegs();
            }
        } else {
            bool oldNeeded = resultNeeded;
            resultNeeded = true;
            node.operand->accept(*this);
            resultNeeded = oldNeeded;
            invalidateRegs();
        }
    } else {
        bool oldNeeded = resultNeeded;
        resultNeeded = true;
        node.operand->accept(*this);
        resultNeeded = oldNeeded;
        if (node.op == "!") {
            std::string labelFalse = newDontCareLabel(); 
            std::string labelEnd = newDontCareLabel();
            if (flags.znSource != FlagSource::A) {
                emit("CMP #$00");
                updateZNFlags(FlagSource::A);
            }
            emit("BNE " + labelFalse); 
            emitter->txa(); 
            updateZNFlags(FlagSource::A);
            emit("BNE " + labelFalse);
            emitter->lda_imm(1); emitter->bra(0x02);
            out << labelFalse << ":" << std::endl; emitter->lda_imm(0);
            out << labelEnd << ":" << std::endl; 
            invalidateRegs();
            emitter->ldx_imm(0);
            updateRegX(0);
        } else if (node.op == "~") { emitter->not_16(); }
        else if (node.op == "-") { emitter->neg_16(); }
    }
    invalidateRegs();
}

void CodeGenerator::visit(AsmStatement& node) {
    embedSource(node);
    emit(node.code);
    invalidateRegs();
}

void CodeGenerator::invalidateRegs() {
    regA.known = false; regA.isVariable = false; regA.varName = ""; regA.varOffset = 0; regA.value = 0;
    regX.known = false; regX.isVariable = false; regX.varName = ""; regX.varOffset = 0; regX.value = 0;
    regY.known = false; regY.isVariable = false; regY.varName = ""; regY.varOffset = 0; regY.value = 0;
    regZ.known = false; regZ.isVariable = false; regZ.varName = ""; regZ.varOffset = 0; regZ.value = 0;
    invalidateFlags();
}

void CodeGenerator::updateFlags(TriState c, TriState z, TriState n, TriState v) {
    if (c != TriState::UNKNOWN) flags.carry = c;
    if (z != TriState::UNKNOWN) flags.zero = z;
    if (n != TriState::UNKNOWN) flags.negative = n;
    if (v != TriState::UNKNOWN) flags.overflow = v;
    if (z != TriState::UNKNOWN || n != TriState::UNKNOWN) flags.znSource = FlagSource::NONE;
}

void CodeGenerator::updateZNFlags(FlagSource source, TriState z, TriState n) {
    flags.znSource = source;
    flags.zero = z;
    flags.negative = n;
}

void CodeGenerator::invalidateFlags() {
    flags.carry = TriState::UNKNOWN;
    flags.zero = TriState::UNKNOWN;
    flags.negative = TriState::UNKNOWN;
    flags.overflow = TriState::UNKNOWN;
    flags.znSource = FlagSource::NONE;
}

