#include "CodeGenerator.hpp"
#include <iostream>

CodeGenerator::CodeGenerator(std::ostream& out) : out(out) {}

void CodeGenerator::generate(TranslationUnit& unit) {
    unit.accept(*this);
    emitData();
}

void CodeGenerator::emit(const std::string& line) {
    out << "    " << line << std::endl;
}

std::string CodeGenerator::newLabel() {
    return "L" + std::to_string(labelCount++);
}

// Track variables in scope
static std::vector<std::string> currentVars;

void CodeGenerator::visit(TranslationUnit& node) {
    out << "; Generated by cc45" << std::endl;
    out << ".org $2000" << std::endl << std::endl;
    for (auto& func : node.functions) func->accept(*this);
}

void CodeGenerator::visit(FunctionDeclaration& node) {
    std::string procLine = "PROC " + node.name;
    currentVars.clear();
    variableTypes.clear();
    for (const auto& param : node.parameters) {
        variableTypes[param.name] = {param.type, param.isPointer};
        if (param.isPointer) {
            procLine += ", W#" + param.name;
        } else if (param.type == "char") {
            procLine += ", B#" + param.name;
        } else {
            procLine += ", W#" + param.name;
        }
        currentVars.push_back(param.name);
    }
    out << procLine << std::endl;
    node.body->accept(*this);
    emit("ENDPROC");
    out << std::endl;
}

void CodeGenerator::visit(AsmStatement& node) {
    out << "; Inline Assembly" << std::endl;
    out << node.code << std::endl;
}

void CodeGenerator::visit(CompoundStatement& node) {
    for (auto& stmt : node.statements) stmt->accept(*this);
}

void CodeGenerator::visit(VariableDeclaration& node) {
    variableTypes[node.name] = {node.type, node.isPointer};
    int size = (node.isPointer) ? 2 : ((node.type == "char") ? 1 : 2);
    if (node.initializer) {
        node.initializer->accept(*this);
        if (size == 2) {
            emit("PHX");
            emit("PHA");
        } else {
            emit("PHA");
        }
    } else {
        if (size == 2) emit("PHW #$0000");
        else {
            emit("LDA #$00");
            emit("PHA");
        }
    }
    for (const auto& varName : currentVars) {
        emit(".var " + varName + " = " + varName + " + " + std::to_string(size));
    }
    emit(".var " + node.name + " = " + std::to_string(size));
    currentVars.push_back(node.name);
    emit(".cleanup " + std::to_string(size));
}

void CodeGenerator::visit(Assignment& node) {
    node.expression->accept(*this);
    emit("STA " + node.name + ", s");
    if (variableTypes.count(node.name) && variableTypes[node.name].type != "char") {
        emit("STX " + node.name + "+1, s");
    }
}

void CodeGenerator::visit(BinaryOperation& node) {
    // Check for +/- 1 optimization
    bool isLiteralOne = false;
    if (auto* lit = dynamic_cast<IntegerLiteral*>(node.right.get())) {
        if (lit->value == 1) isLiteralOne = true;
    }

    if (isLiteralOne && (node.op == "+" || node.op == "-")) {
        node.left->accept(*this); // Evaluates LHS into A/X
        if (node.op == "+") {
            std::string label = newLabel();
            emit("INC A");
            emit("BNE " + label);
            emit("INX");
            out << label << ":" << std::endl;
        } else {
            std::string label = newLabel();
            emit("CMP #$00");
            emit("BNE " + label);
            emit("DEX");
            out << label << ":" << std::endl;
            emit("DEC A");
        }
        return;
    }

    // 1. Evaluate LHS and push result
    node.left->accept(*this);
    // Find LHS type if it's a variable reference to optimize push
    int lhsSize = 2;
    if (auto* ref = dynamic_cast<VariableReference*>(node.left.get())) {
        if (variableTypes.count(ref->name) && variableTypes[ref->name].type == "char" && !variableTypes[ref->name].isPointer) {
            lhsSize = 1;
        }
    }

    if (lhsSize == 2) {
        emit("PHX");
        emit("PHA");
    } else {
        emit("PHA");
    }
    for (const auto& varName : currentVars) emit(".var " + varName + " = " + varName + " + " + std::to_string(lhsSize));
    
    // 2. Evaluate RHS into A/X
    node.right->accept(*this);
    
    // 3. Perform operation using A/X and value at TOP of stack
    // (LHS is at 1,s if 8-bit, or 2,s if 16-bit)
    if (node.op == "+") {
        if (lhsSize == 1) {
            emit("CLC");
            emit("ADC 1, s");
            emit("LDX #$00"); // Result high is 0 for char + anything? 
            // Actually char + int should be int. But we are in a char context.
            // Simplified: result is always 16-bit in registers for now.
        } else {
            emit("STA $02");
            emit("STX $03");
            emit("LDA 2, s");
            emit("CLC");
            emit("ADC $02"); 
            emit("PHA");
            emit("LDA 3, s");
            emit("ADC $03");
            emit("TAX");
            emit("PLA");
        }
    } else if (node.op == "-") {
        if (lhsSize == 1) {
            emit("STA $02");
            emit("LDA 1, s");
            emit("SEC");
            emit("SBC $02");
            emit("LDX #$00");
        } else {
            emit("STA $02");
            emit("STX $03");
            emit("LDA 2, s");
            emit("SEC");
            emit("SBC $02");
            emit("PHA");
            emit("LDA 3, s");
            emit("SBC $03");
            emit("TAX");
            emit("PLA");
        }
    } else if (node.op == "==") {
        if (lhsSize == 1) {
            emit("CMP 1, s");
        } else {
            emit("CMP 2, s");
        }
        std::string labelFalse = newLabel();
        std::string labelEnd = newLabel();
        emit("BNE " + labelFalse);
        if (lhsSize == 2) {
            emit("TXA");
            emit("CMP 3, s");
            emit("BNE " + labelFalse);
        }
        emit("LDA #$01");
        emit("BRA " + labelEnd);
        out << labelFalse << ":" << std::endl;
        emit("LDA #$00");
        out << labelEnd << ":" << std::endl;
        emit("LDX #$00");
    } else if (node.op == "*") {
        // ... use hardware math as before but with correct LHS source ...
        emit("STA $D770");
        emit("STX $D771");
        if (lhsSize == 1) {
            emit("LDA 1, s");
            emit("STA $D774");
            emit("STZ $D775");
        } else {
            emit("LDA 2, s");
            emit("STA $D774");
            emit("LDA 3, s");
            emit("STA $D775");
        }
        emit("LDA $D778");
        emit("LDX $D779");
    } else if (node.op == "/") {
        emit("STA $D764");
        emit("STX $D765");
        if (lhsSize == 1) {
            emit("LDA 1, s");
            emit("STA $D760");
            emit("STZ $D761");
        } else {
            emit("LDA 2, s");
            emit("STA $D760");
            emit("LDA 3, s");
            emit("STA $D761");
        }
        emit("LDA $D768");
        emit("LDX $D769");
    } else if (node.op == "!=") {
        if (lhsSize == 1) {
            emit("CMP 1, s");
        } else {
            emit("CMP 2, s");
        }
        std::string labelTrue = newLabel();
        std::string labelEnd = newLabel();
        emit("BNE " + labelTrue);
        if (lhsSize == 2) {
            emit("TXA");
            emit("CMP 3, s");
            emit("BNE " + labelTrue);
        }
        emit("LDA #$00");
        emit("BRA " + labelEnd);
        out << labelTrue << ":" << std::endl;
        emit("LDA #$01");
        out << labelEnd << ":" << std::endl;
        emit("LDX #$00");
    } else if (node.op == "<") {
        if (lhsSize == 1) {
            emit("STA $02");
            emit("LDA 1, s");
            emit("CMP $02");
        } else {
            emit("STA $02");
            emit("STX $03");
            emit("LDA 3, s");
            emit("CMP $03");
            std::string labelTrue = newLabel();
            std::string labelFalse = newLabel();
            std::string labelEnd = newLabel();
            emit("BCC " + labelTrue);
            emit("BNE " + labelFalse);
            emit("LDA 2, s");
            emit("CMP $02");
            emit("BCC " + labelTrue);
            out << labelFalse << ":" << std::endl;
            emit("LDA #$00");
            emit("BRA " + labelEnd);
            out << labelTrue << ":" << std::endl;
            emit("LDA #$01");
            out << labelEnd << ":" << std::endl;
        }
        if (lhsSize == 1) {
            std::string labelTrue = newLabel();
            std::string labelEnd = newLabel();
            emit("BCC " + labelTrue);
            emit("LDA #$00");
            emit("BRA " + labelEnd);
            out << labelTrue << ":" << std::endl;
            emit("LDA #$01");
            out << labelEnd << ":" << std::endl;
        }
        emit("LDX #$00");
    } else if (node.op == ">") {
        if (lhsSize == 1) {
            emit("STA $02");
            emit("LDA $02");
            emit("CMP 1, s");
        } else {
            emit("STA $02");
            emit("STX $03");
            emit("LDA $03");
            emit("CMP 3, s");
            std::string labelTrue = newLabel();
            std::string labelFalse = newLabel();
            std::string labelEnd = newLabel();
            emit("BCC " + labelTrue); 
            emit("BNE " + labelFalse);
            emit("LDA $02");
            emit("CMP 2, s");
            emit("BCC " + labelTrue);
            out << labelFalse << ":" << std::endl;
            emit("LDA #$00");
            emit("BRA " + labelEnd);
            out << labelTrue << ":" << std::endl;
            emit("LDA #$01");
            out << labelEnd << ":" << std::endl;
        }
        if (lhsSize == 1) {
            std::string labelTrue = newLabel();
            std::string labelEnd = newLabel();
            emit("BCC " + labelTrue);
            emit("LDA #$00");
            emit("BRA " + labelEnd);
            out << labelTrue << ":" << std::endl;
            emit("LDA #$01");
            out << labelEnd << ":" << std::endl;
        }
        emit("LDX #$00");
    } else if (node.op == "<=") {
        if (lhsSize == 1) {
            emit("STA $02");
            emit("LDA 1, s");
            emit("CMP $02");
            std::string labelTrue = newLabel();
            std::string labelEnd = newLabel();
            emit("BCC " + labelTrue);
            emit("BEQ " + labelTrue);
            emit("LDA #$00");
            emit("BRA " + labelEnd);
            out << labelTrue << ":" << std::endl;
            emit("LDA #$01");
            out << labelEnd << ":" << std::endl;
        } else {
            emit("STA $02");
            emit("STX $03");
            emit("LDA 3, s");
            emit("CMP $03");
            std::string labelTrue = newLabel();
            std::string labelFalse = newLabel();
            std::string labelEnd = newLabel();
            emit("BCC " + labelTrue);
            emit("BNE " + labelFalse);
            emit("LDA 2, s");
            emit("CMP $02");
            emit("BCC " + labelTrue);
            emit("BEQ " + labelTrue);
            out << labelFalse << ":" << std::endl;
            emit("LDA #$00");
            emit("BRA " + labelEnd);
            out << labelTrue << ":" << std::endl;
            emit("LDA #$01");
            out << labelEnd << ":" << std::endl;
        }
        emit("LDX #$00");
    } else if (node.op == ">=") {
        if (lhsSize == 1) {
            emit("STA $02");
            emit("LDA 1, s");
            emit("CMP $02");
            std::string labelTrue = newLabel();
            std::string labelEnd = newLabel();
            emit("BCS " + labelTrue);
            emit("LDA #$00");
            emit("BRA " + labelEnd);
            out << labelTrue << ":" << std::endl;
            emit("LDA #$01");
            out << labelEnd << ":" << std::endl;
        } else {
            emit("STA $02");
            emit("STX $03");
            emit("LDA 3, s");
            emit("CMP $03");
            std::string labelTrue = newLabel();
            std::string labelFalse = newLabel();
            std::string labelEnd = newLabel();
            emit("BCS " + labelTrue); 
            emit("BCC " + labelFalse);
            emit("BNE " + labelTrue);
            emit("LDA 2, s");
            emit("CMP $02");
            emit("BCS " + labelTrue);
            out << labelFalse << ":" << std::endl;
            emit("LDA #$00");
            emit("BRA " + labelEnd);
            out << labelTrue << ":" << std::endl;
            emit("LDA #$01");
            out << labelEnd << ":" << std::endl;
        }
        emit("LDX #$00");
    }
    
    // 4. Pop the LHS
    emit("RTN #" + std::to_string(lhsSize));
    for (const auto& varName : currentVars) emit(".var " + varName + " = " + varName + " - " + std::to_string(lhsSize));
}

void CodeGenerator::visit(ReturnStatement& node) {
    if (node.expression) node.expression->accept(*this);
    emit("ENDPROC");
}

void CodeGenerator::visit(ExpressionStatement& node) {
    node.expression->accept(*this);
}

void CodeGenerator::visit(IfStatement& node) {
    std::string labelElse = newLabel();
    std::string labelEnd = newLabel();
    
    node.condition->accept(*this);
    emit("CMP #$00");
    emit("BEQ " + labelElse);
    
    node.thenBranch->accept(*this);
    if (node.elseBranch) {
        emit("BRA " + labelEnd);
    }
    
    out << labelElse << ":" << std::endl;
    if (node.elseBranch) {
        node.elseBranch->accept(*this);
        out << labelEnd << ":" << std::endl;
    }
}

void CodeGenerator::visit(WhileStatement& node) {
    std::string labelStart = newLabel();
    std::string labelEnd = newLabel();
    
    out << labelStart << ":" << std::endl;
    node.condition->accept(*this);
    emit("CMP #$00");
    emit("BEQ " + labelEnd);
    
    node.body->accept(*this);
    emit("BRA " + labelStart);
    
    out << labelEnd << ":" << std::endl;
}

void CodeGenerator::visit(DoWhileStatement& node) {
    std::string labelStart = newLabel();
    
    out << labelStart << ":" << std::endl;
    node.body->accept(*this);
    
    node.condition->accept(*this);
    emit("CMP #$00");
    emit("BNE " + labelStart);
}

void CodeGenerator::visit(ForStatement& node) {
    std::string labelStart = newLabel();
    std::string labelEnd = newLabel();
    
    if (node.initializer) {
        node.initializer->accept(*this);
    }
    
    out << labelStart << ":" << std::endl;
    if (node.condition) {
        node.condition->accept(*this);
        emit("CMP #$00");
        emit("BEQ " + labelEnd);
    }
    
    node.body->accept(*this);
    
    if (node.increment) {
        node.increment->accept(*this);
    }
    
    emit("BRA " + labelStart);
    out << labelEnd << ":" << std::endl;
}

void CodeGenerator::visit(FunctionCall& node) {
    int bytesPushed = 0;
    for (auto& arg : node.arguments) {
        arg->accept(*this);
        emit("PHX");
        emit("PHA");
        bytesPushed += 2;
        for (const auto& varName : currentVars) emit(".var " + varName + " = " + varName + " + 2");
    }
    emit("JSR " + node.name);
    if (bytesPushed > 0) {
        for (const auto& varName : currentVars) emit(".var " + varName + " = " + varName + " - " + std::to_string(bytesPushed));
    }
}

void CodeGenerator::visit(VariableReference& node) {
    emit("LDA " + node.name + ", s");
    if (variableTypes.count(node.name) && variableTypes[node.name].type == "char" && !variableTypes[node.name].isPointer) {
        emit("LDX #$00");
    } else {
        emit("LDX " + node.name + "+1, s");
    }
}

void CodeGenerator::visit(IntegerLiteral& node) {
    emit("LDA #$" + std::to_string(node.value & 0xFF));
    emit("LDX #$" + std::to_string((node.value >> 8) & 0xFF));
}

void CodeGenerator::visit(StringLiteral& node) {
    if (stringPool.find(node.value) == stringPool.end()) {
        stringPool[node.value] = "str" + std::to_string(stringCount++);
    }
    std::string label = stringPool[node.value];
    emit("LDA #<" + label);
    emit("LDX #>" + label);
}

void CodeGenerator::emitData() {
    if (stringPool.empty()) return;
    out << "; Data Section" << std::endl;
    for (const auto& entry : stringPool) {
        out << entry.second << ":" << std::endl;
        out << "    .text \"" << entry.first << "\"" << std::endl;
        out << "    .byte 0" << std::endl;
    }
}
